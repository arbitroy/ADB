    private void staffAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_staffAddMouseClicked
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String dateString = hire_date.getText();
        java.util.Date parsedDate = null;
        try {
            parsedDate = format.parse(dateString);
        } catch (ParseException ex) {
            Logger.getLogger(AdminDashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
        java.sql.Date sqlDate = new java.sql.Date(parsedDate.getTime());
        try{
            Con =DriverManager.getConnection("jdbc:postgresql://localhost:5432/AmazingDessertBar", "postgres", "1234");
            PreparedStatement add = Con.prepareStatement("BEGIN;\n" +
"WITH new_staff AS (\n" +
"  INSERT INTO Staff (FirstName, LastName, JobRole, Hire_date)\n" +
"  VALUES (?, ?, ?, ?)\n" +
"  RETURNING StaffID\n" +
")\n" +
"INSERT INTO Staff_Login (StaffID, username, password)\n" +
"SELECT StaffID, ?, ?\n" +
"FROM new_staff;\n" +
"COMMIT;");
            add.setString(1, f_name.getText());
            add.setString(2, l_name.getText());
            add.setString(3, job_role.getText());
            add.setDate(4, sqlDate);
            add.setString(5, u_name.getText());
            add.setString(6, pass.getText());
            add.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"Staff Sucessfully Added");
            
        }catch(SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_staffAddMouseClicked

